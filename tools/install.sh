main() {
  # Use colors, but only if connected to a terminal, and that terminal
  # supports them.
  if which tput >/dev/null 2>&1; then
      ncolors=$(tput colors)
  fi
  if [ -t 1 ] && [ -n "$ncolors" ] && [ "$ncolors" -ge 8 ]; then
    RED="$(tput setaf 1)"
    GREEN="$(tput setaf 2)"
    YELLOW="$(tput setaf 3)"
    BLUE="$(tput setaf 4)"
    BOLD="$(tput bold)"
    NORMAL="$(tput sgr0)"
  else
    RED=""
    GREEN=""
    YELLOW=""
    BLUE=""
    BOLD=""
    NORMAL=""
  fi

  # Only enable exit-on-error after the non-critical colorization stuff,
  # which may fail on systems lacking tput or terminfo
  set -e

  CHECK_ZSH_INSTALLED=$(grep /zsh$ /etc/shells | wc -l)
  if [ ! $CHECK_ZSH_INSTALLED -ge 1 ]; then
    printf "${YELLOW}Zsh is not installed!${NORMAL} Please install zsh first!\n"
    exit
  fi
  unset CHECK_ZSH_INSTALLED

  CHECK_VIRTUALENV_INSTALLED=$(which virtualenv)
  if [ ! $CHECK_VIRTUALENV_INSTALLED -ge 1 ]; then
    printf "${YELLOW}virtualenv is not installed!${NORMAL} Please install virtualenv first!\n"
    exit
  fi
  unset CHECK_VIRTUALENV_INSTALLED

  if [ ! -n "$CISTERN" ]; then
    CISTERN=~/cistern
  fi

  if [ -d "$CISTERN" ]; then
    printf "${YELLOW}You already have cistern installed.${NORMAL}\n"
    printf "You'll need to remove $CISTERN if you want to re-install.\n"
    exit
  fi

  # Prevent the cloned repository from having insecure permissions. Failing to do
  # so causes compinit() calls to fail with "command not found: compdef" errors
  # for users with insecure umasks (e.g., "002", allowing group writability). Note
  # that this will be ignored under Cygwin by default, as Windows ACLs take
  # precedence over umasks except for filesystems mounted with option "noacl".
  umask g-w,o-w

  printf "${BLUE}Cloning cistern...${NORMAL}\n"
  hash git >/dev/null 2>&1 || {
    echo "Error: git is not installed"
    exit 1
  }
  # The Windows (MSYS) Git is not compatible with normal use on cygwin
  if [ "$OSTYPE" = cygwin ]; then
    if git --version | grep msysgit > /dev/null; then
      echo "Error: Windows/MSYS Git is not supported on Cygwin"
      echo "Error: Make sure the Cygwin git package is installed and is first on the path"
      exit 1
    fi
  fi
  env git clone --depth=1 https://github.com/magicmark/cistern.git $CISTERN || {
    printf "Error: git clone of cistern repo failed\n"
    exit 1
  }

  # set origin url
  git -C $CISTERN remote set-url origin git@github.com:magicmark/cistern

  printf "${BLUE}Looking for an existing zsh config...${NORMAL}\n"
  if [ -f ~/.zshrc ] || [ -h ~/.zshrc ]; then
    printf "${YELLOW}Found ~/.zshrc.${NORMAL} ${GREEN}Backing up to ~/.zshrc.pre-cistern${NORMAL}\n";
    mv ~/.zshrc ~/.zshrc.pre-cistern;
  fi

  cd $CISTERN
  make run

  printf "${GREEN}"
  echo '~~~ cicstern is now installed ~~~'
  echo ''
  printf "${NORMAL}"
  env zsh
}

main
